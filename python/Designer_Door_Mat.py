x=input()
x=x.split()
n=int(x[0])
m=int(x[1])
lst = []
for i in range(1,n//2+1):
    temp=""
    for j in range(2*i-1):
        temp = temp+".|."
    lst.append(temp.center(m,"-"))
lst.append("WELCOME".center(m,"-"))
for x in lst:
    print(x)
lst.reverse()
for i in range(1,len(lst)):
    print(lst[i])

